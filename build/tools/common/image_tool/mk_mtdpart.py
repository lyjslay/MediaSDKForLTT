#!/usr/bin/python
# -*- coding: utf-8 -*-
import argparse
import logging
import os
import sys
from XmlParser import XmlParser
FORMAT = '%(levelname)s: %(message)s'
logging.basicConfig(level=logging.INFO, format=FORMAT)


def main():
    parser = argparse.ArgumentParser(description='Create mtdpart.h')
    parser.add_argument('xml', help='path to partition xml')
    parser.add_argument('output', help='output folder')
    args = parser.parse_args()
    parser = XmlParser(args.xml)
    parts = parser.parse()
    with open(os.path.join(args.output, "mtdpart.h"), "w") as of:
        of.write("/* this file should be generated by mk_mtdpart.py,")
        of.write("please do not modify this file manually*/\n\n")
        of.write("#define MTDPARTS_DEFAULT    ")
        of.write("\"mtdparts=cvsnfc:")
        for p in parts:
            if p["part_size"] != sys.maxsize:
                p["part_size"]= int(p["part_size"]/1024)
                part_size = str(p["part_size"]) + "K"
            else:
                part_size = "-"
            if part_size == "-":
                comma = "\"\n"
            else:
                comma = ","
            of.write("%s(%s)%s" % (part_size, p["label"], comma))
    logging.info("generating mtdpart.h")


if __name__ == "__main__":
    main()
